'''
Crie um programa que leia dois números do usuário e exiba a soma desses números.
'''
n1 = float(input('Digite um número qualquer para relizar a soma: '))
n2 = float(input('Digite outro número para realizar a soma: '))
soma = n1 + n2
print('O resultado da soma de {} e {} foi: {}'.format(n1,n2,soma))